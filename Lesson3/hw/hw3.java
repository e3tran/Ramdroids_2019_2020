public class hw3 {
	public static void main( String[] args ) {
	    for( int x = 0; x < 5; x++ ) {
	        System.out.print( "*" );
	        }
	    System.out.println(); // Skip a line
	    for( int x = 0; x < 5; x++ ) {
	        System.out.print( "*" );
	        }
	    System.out.println();
	    for( int x = 0; x < 5; x++ ) {
	        System.out.print( "*" );
	        }
	    System.out.println();
	    for( int x = 0; x < 5; x++ ) {
	        System.out.print( "*" );
	        }
	    System.out.println();
	    for( int x = 0; x < 5; x++ ) {
	        System.out.print( "*" );
	        }
	    }
	}
